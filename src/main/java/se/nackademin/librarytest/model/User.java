
package se.nackademin.librarytest.model;

public class User {
    private String userFirstName;
    private String userLastName;
    private String userPhoneNr;
    private String userEmail;

    /**
     * @return the firstName
     */
    public String getUserFirstName() {
        return userFirstName;
    }

    /**
     * @param userFirstName the firstName to set
     */
    public void setUserFirstName(String userFirstName) {
        this.userFirstName = userFirstName;
    }

    /**
     * @return the lastName
     */
    public String getUserLastName() {
        return userLastName;
    }

    /**
     * @param userLastName the lastName to set
     */
    public void setUserLastName(String userLastName) {
        this.userLastName = userLastName;
    }

    /**
     * @return the phoneNr
     */
    public String getUserPhoneNr() {
        return userPhoneNr;
    }

    /**
     * @param userPhoneNr the phoneNr to set
     */
    public void setUserPhoneNr(String userPhoneNr) {
        this.userPhoneNr = userPhoneNr;
    }

    /**
     * @return the email
     */
    public String getUserEmail() {
        return userEmail;
    }

    /**
     * @param userEmail the email to set
     */
    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }
    
    
    
}
